def _get_google_service(api: str, version: str) -> Any:
    """Build Google API service from credentials file. Returns None if not configured."""
    if not _CREDS_PATH.exists():
        return None

    try:
        import google.oauth2.service_account as sa
        from googleapiclient.discovery import build

        creds_data = json.loads(_CREDS_PATH.read_text())
        if creds_data.get("type") == "service_account":
            creds = sa.Credentials.from_service_account_info(
                creds_data,
                scopes=[
                    "https://www.googleapis.com/auth/drive",
                    "https://www.googleapis.com/auth/spreadsheets",
                ],
            )
        else:
            # OAuth token (from /drive-auth flow)
            from google.oauth2.credentials import Credentials
            creds = Credentials.from_authorized_user_info(creds_data)

        return build(api, version, credentials=creds, cache_discovery=False)
    except Exception as e:
        logger.warning("google_service_error", api=api, error=str(e))
        return None


def _get_or_create_folder(drive_svc: Any, name: str, parent_id: Optional[str] = None) -> str:
    """Get existing Drive folder by name+parent or create it. Returns folder ID."""
    query = f"name='{name}' and mimeType='application/vnd.google-apps.folder' and trashed=false"
    if parent_id:
        query += f" and '{parent_id}' in parents"

    results = drive_svc.files().list(q=query, fields="files(id,name)").execute()
    files = results.get("files", [])
    if files:
        return files[0]["id"]

    # Create folder
    meta = {
        "name": name,
        "mimeType": "application/vnd.google-apps.folder",
    }
    if parent_id:
        meta["parents"] = [parent_id]

    folder = drive_svc.files().create(body=meta, fields="id").execute()
    folder_id = folder["id"]
    logger.info("drive_folder_created", name=name, id=folder_id)
    return folder_id


def _folder_url(folder_id: str) -> str:
    return f"https://drive.google.com/drive/folders/{folder_id}"


def _file_url(file_id: str) -> str:
    return f"https://drive.google.com/file/d/{file_id}/view"


def _get_publication_folder(drive_svc: Any, pub: Publication) -> tuple[str, str]:
    """Get or create the Drive folder for this publication. Returns (folder_id, folder_url).

    Structure:
      AURA Social Media/
        2026/
          Q2/
            April/
              Week-16/
                2026-04-12_19:00_instagram_1:1/
    """
    # Root folder: "Social Media" inside the existing AURA folder
    root_id_path = _DRIVE_ROOT_ID_PATH
    if root_id_path.exists():
        root_id = root_id_path.read_text().strip()
    else:
        # Use existing AURA folder as parent if available
        aura_parent = None
        if _AURA_PARENT_ID_PATH.exists():
            aura_parent = _AURA_PARENT_ID_PATH.read_text().strip() or None
        root_id = _get_or_create_folder(drive_svc, _DRIVE_ROOT_NAME, aura_parent)
        root_id_path.write_text(root_id)

    # Date-based hierarchy from scheduled_at or created_at
    ref_dt = pub.scheduled_at or pub.created_at
    year = ref_dt.strftime("%Y")
    quarter = f"Q{(ref_dt.month - 1) // 3 + 1}"
    month = ref_dt.strftime("%B")   # "April"
    week_num = ref_dt.isocalendar()[1]
    week = f"Week-{week_num:02d}"

    # Publication-specific folder: "2026-04-12_19:00_instagram_1x1"
    time_str = (pub.scheduled_at or pub.created_at).strftime("%Y-%m-%d_%H:%M")
    fmt_safe = pub.format.replace(":", "x")
    pub_folder_name = f"{time_str}_{pub.platform}_{fmt_safe}"

    # Build hierarchy
    year_id = _get_or_create_folder(drive_svc, year, root_id)
    quarter_id = _get_or_create_folder(drive_svc, quarter, year_id)
    month_id = _get_or_create_folder(drive_svc, month, quarter_id)
    week_id = _get_or_create_folder(drive_svc, week, month_id)
    pub_folder_id = _get_or_create_folder(drive_svc, pub_folder_name, week_id)

    return pub_folder_id, _folder_url(pub_folder_id)


def _upload_file_to_drive(
    drive_svc: Any,
    file_bytes: bytes,
    filename: str,
    mime_type: str,
    parent_folder_id: str,
) -> tuple[str, str]:
    """Upload bytes to Drive. Returns (file_id, file_url)."""
    import io
    from googleapiclient.http import MediaIoBaseUpload

    media = MediaIoBaseUpload(io.BytesIO(file_bytes), mimetype=mime_type)
    meta = {"name": filename, "parents": [parent_folder_id]}
    result = drive_svc.files().create(
        body=meta, media_body=media, fields="id"
    ).execute()
    file_id = result["id"]
    # Make readable by anyone with link
    drive_svc.permissions().create(
        fileId=file_id,
        body={"role": "reader", "type": "anyone"},
    ).execute()
    return file_id, _file_url(file_id)


