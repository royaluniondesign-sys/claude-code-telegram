# ---------------------------------------------------------------------------
# Data model
# ---------------------------------------------------------------------------

@dataclass
class Publication:
    platform: str                    # instagram | twitter | linkedin
    format: str                      # 1:1 | 4:3 | 9:16
    topic: str
    headline: str
    subheadline: str
    caption: str
    tag: str
    status: str = "generated"        # generated | scheduled | published | failed
    post_url: str = ""
    drive_folder_id: str = ""
    drive_folder_url: str = ""
    drive_image_id: str = ""
    drive_image_url: str = ""
    drive_video_id: str = ""
    drive_video_url: str = ""
    likes: int = 0
    comments: int = 0
    shares: int = 0
    notes: str = ""
    scheduled_at: Optional[datetime] = None
    published_at: Optional[datetime] = None
    pub_id: str = ""
    created_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


# ---------------------------------------------------------------------------
# SQLite layer (always available)
# ---------------------------------------------------------------------------

def _init_db() -> sqlite3.Connection:
    _DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(str(_DB_PATH), detect_types=sqlite3.PARSE_DECLTYPES)
    conn.execute("""
        CREATE TABLE IF NOT EXISTS publications (
            id          TEXT PRIMARY KEY,
            created_at  TEXT NOT NULL,
            scheduled_at TEXT,
            published_at TEXT,
            platform    TEXT NOT NULL,
            format      TEXT NOT NULL,
            topic       TEXT NOT NULL,
            headline    TEXT,
            subheadline TEXT,
            caption     TEXT,
            tag         TEXT,
            status      TEXT NOT NULL DEFAULT 'generated',
            post_url    TEXT DEFAULT '',
            drive_folder_id   TEXT DEFAULT '',
            drive_folder_url  TEXT DEFAULT '',
            drive_image_id    TEXT DEFAULT '',
            drive_image_url   TEXT DEFAULT '',
            drive_video_id    TEXT DEFAULT '',
            drive_video_url   TEXT DEFAULT '',
            likes       INTEGER DEFAULT 0,
            comments    INTEGER DEFAULT 0,
            shares      INTEGER DEFAULT 0,
            notes       TEXT DEFAULT '',
            sheet_row   INTEGER DEFAULT 0
        )
    """)
    conn.commit()
    return conn


def _gen_pub_id(platform: str) -> str:
    import time
    ts = int(time.time())
    return f"{platform[:2].upper()}-{ts}"


def _save_to_sqlite(pub: Publication) -> str:
    pub_id = pub.pub_id or _gen_pub_id(pub.platform)
    pub.pub_id = pub_id
    conn = _init_db()
    conn.execute("""
        INSERT OR REPLACE INTO publications (
            id, created_at, scheduled_at, published_at,
            platform, format, topic, headline, subheadline,
            caption, tag, status, post_url,
            drive_folder_id, drive_folder_url,
            drive_image_id, drive_image_url,
            drive_video_id, drive_video_url,
            likes, comments, shares, notes
        ) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)
    """, (
        pub_id,
        pub.created_at.isoformat(),
        pub.scheduled_at.isoformat() if pub.scheduled_at else None,
        pub.published_at.isoformat() if pub.published_at else None,
        pub.platform, pub.format, pub.topic,
        pub.headline, pub.subheadline, pub.caption, pub.tag,
        pub.status, pub.post_url,
        pub.drive_folder_id, pub.drive_folder_url,
        pub.drive_image_id, pub.drive_image_url,
        pub.drive_video_id, pub.drive_video_url,
        pub.likes, pub.comments, pub.shares, pub.notes,
    ))
    conn.commit()
    conn.close()
    logger.info("publication_saved_sqlite", pub_id=pub_id, platform=pub.platform)
    return pub_id


def get_recent_publications(limit: int = 20) -> list[dict[str, Any]]:
    """Return recent publications from SQLite as list of dicts."""
    conn = _init_db()
    cur = conn.execute(
        "SELECT * FROM publications ORDER BY created_at DESC LIMIT ?", (limit,)
    )
    cols = [d[0] for d in cur.description]
    rows = [dict(zip(cols, row)) for row in cur.fetchall()]
    conn.close()
    return rows


def update_publication_status(
    pub_id: str,
    status: str,
    post_url: str = "",
    published_at: Optional[datetime] = None,
) -> None:
    """Update status + post URL after publishing."""
    conn = _init_db()
    conn.execute("""
        UPDATE publications
        SET status=?, post_url=?, published_at=?
        WHERE id=?
    """, (
        status,
        post_url,
        published_at.isoformat() if published_at else None,
        pub_id,
    ))
    conn.commit()
    conn.close()


