def _get_or_create_spreadsheet(sheets_svc: Any, drive_svc: Any) -> str:
    """Get existing spreadsheet ID or create new one. Returns sheet ID."""
    if _SHEET_ID_PATH.exists():
        return _SHEET_ID_PATH.read_text().strip()

    # Create spreadsheet
    spreadsheet = sheets_svc.spreadsheets().create(body={
        "properties": {"title": _SPREADSHEET_NAME, "locale": "es_MX"},
        "sheets": [{
            "properties": {"title": "Publicaciones", "index": 0},
        }],
    }).execute()
    sheet_id = spreadsheet["spreadsheetId"]

    # Write header row
    sheets_svc.spreadsheets().values().update(
        spreadsheetId=sheet_id,
        range="Publicaciones!A1",
        valueInputOption="USER_ENTERED",
        body={"values": [COLUMNS]},
    ).execute()

    # Format header (bold, frozen, background)
    sheets_svc.spreadsheets().batchUpdate(
        spreadsheetId=sheet_id,
        body={"requests": [
            # Freeze row 1
            {"updateSheetProperties": {
                "properties": {"sheetId": 0, "gridProperties": {"frozenRowCount": 1}},
                "fields": "gridProperties.frozenRowCount",
            }},
            # Bold header
            {"repeatCell": {
                "range": {"sheetId": 0, "startRowIndex": 0, "endRowIndex": 1},
                "cell": {"userEnteredFormat": {
                    "textFormat": {"bold": True},
                    "backgroundColor": {"red": 0.851, "green": 0.467, "blue": 0.341},
                }},
                "fields": "userEnteredFormat(textFormat,backgroundColor)",
            }},
            # Auto-resize columns
            {"autoResizeDimensions": {
                "dimensions": {"sheetId": 0, "dimension": "COLUMNS", "startIndex": 0, "endIndex": len(COLUMNS)},
            }},
        ]},
    ).execute()

    _SHEET_ID_PATH.write_text(sheet_id)
    logger.info("spreadsheet_created", sheet_id=sheet_id, name=_SPREADSHEET_NAME)
    return sheet_id


def _append_to_sheet(sheets_svc: Any, sheet_id: str, pub: Publication) -> None:
    """Append a publication row to the Google Sheet."""
    ref_dt = pub.scheduled_at or pub.created_at
    row = [
        pub.pub_id,
        pub.created_at.strftime("%Y-%m-%d %H:%M:%S UTC"),
        ref_dt.strftime("%Y-%m-%d %H:%M") if pub.scheduled_at else "",
        pub.platform.capitalize(),
        pub.format,
        pub.topic,
        pub.headline,
        pub.subheadline,
        pub.caption,
        pub.tag,
        pub.status,
        pub.post_url,
        pub.drive_folder_url,
        pub.drive_image_url,
        pub.drive_video_url,
        pub.likes,
        pub.comments,
        pub.shares,
        pub.notes,
    ]
    sheets_svc.spreadsheets().values().append(
        spreadsheetId=sheet_id,
        range="Publicaciones!A1",
        valueInputOption="USER_ENTERED",
        insertDataOption="INSERT_ROWS",
        body={"values": [row]},
    ).execute()
    logger.info("sheet_row_appended", pub_id=pub.pub_id)


