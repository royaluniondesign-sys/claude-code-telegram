class PublicationDB:
    """Central publication tracker. SQLite always, Drive+Sheets when configured."""

    def __init__(self) -> None:
        _init_db()

    async def log_publication(
        self,
        platform: str,
        format: str,
        topic: str,
        headline: str,
        subheadline: str,
        caption: str,
        tag: str,
        image_bytes: Optional[bytes] = None,
        video_bytes: Optional[bytes] = None,
        video_url: str = "",
        scheduled_at: Optional[datetime] = None,
        status: str = "generated",
        notes: str = "",
    ) -> str:
        """Log a new publication. Returns pub_id.

        Saves to SQLite immediately, then uploads to Drive+Sheets in background.
        """
        pub = Publication(
            platform=platform,
            format=format,
            topic=topic,
            headline=headline,
            subheadline=subheadline,
            caption=caption,
            tag=tag,
            status=status,
            scheduled_at=scheduled_at,
            notes=notes,
        )

        # Step 1: SQLite (immediate, always)
        pub_id = _save_to_sqlite(pub)
        pub.pub_id = pub_id

        # Step 2: Drive + Sheets (background, only if configured)
        if _CREDS_PATH.exists():
            asyncio.create_task(
                self._sync_to_google(pub, image_bytes, video_bytes, video_url)
            )
        else:
            logger.info("google_sync_skipped", reason="no credentials", pub_id=pub_id)

        return pub_id

    async def _sync_to_google(
        self,
        pub: Publication,
        image_bytes: Optional[bytes],
        video_bytes: Optional[bytes],
        video_url: str,
    ) -> None:
        """Upload to Drive + append to Sheets. Runs in background."""
        loop = asyncio.get_event_loop()
        try:
            await loop.run_in_executor(None, self._sync_to_google_sync, pub, image_bytes, video_bytes, video_url)
        except Exception as e:
            logger.error("google_sync_error", pub_id=pub.pub_id, error=str(e))

    def _sync_to_google_sync(
        self,
        pub: Publication,
        image_bytes: Optional[bytes],
        video_bytes: Optional[bytes],
        video_url: str,
    ) -> None:
        """Synchronous Drive+Sheets sync (called from executor)."""
        drive_svc = _get_google_service("drive", "v3")
        sheets_svc = _get_google_service("sheets", "v4")

        if not drive_svc or not sheets_svc:
            logger.warning("google_services_unavailable", pub_id=pub.pub_id)
            return

        # 1. Create publication folder
        folder_id, folder_url = _get_publication_folder(drive_svc, pub)
        pub.drive_folder_id = folder_id
        pub.drive_folder_url = folder_url

        # 2. Upload image
        if image_bytes:
            fmt_safe = pub.format.replace(":", "x")
            filename = f"{pub.pub_id}_{pub.platform}_{fmt_safe}.png"
            img_id, img_url = _upload_file_to_drive(
                drive_svc, image_bytes, filename, "image/png", folder_id
            )
            pub.drive_image_id = img_id
            pub.drive_image_url = img_url

        # 3. Upload video (if bytes provided)
        if video_bytes:
            vid_filename = f"{pub.pub_id}_{pub.platform}_video.mp4"
            vid_id, vid_url = _upload_file_to_drive(
                drive_svc, video_bytes, vid_filename, "video/mp4", folder_id
            )
            pub.drive_video_id = vid_id
            pub.drive_video_url = vid_url

        # 4. Upload metadata JSON
        metadata = {
            "pub_id": pub.pub_id,
            "platform": pub.platform,
            "format": pub.format,
            "topic": pub.topic,
            "headline": pub.headline,
            "subheadline": pub.subheadline,
            "caption": pub.caption,
            "tag": pub.tag,
            "status": pub.status,
            "scheduled_at": pub.scheduled_at.isoformat() if pub.scheduled_at else None,
            "created_at": pub.created_at.isoformat(),
            "drive_folder": folder_url,
            "drive_image": pub.drive_image_url,
            "drive_video": pub.drive_video_url or video_url,
        }
        meta_bytes = json.dumps(metadata, indent=2, ensure_ascii=False).encode()
        _upload_file_to_drive(
            drive_svc, meta_bytes, f"{pub.pub_id}_metadata.json",
            "application/json", folder_id
        )

        # 5. Update SQLite with Drive links
        conn = _init_db()
        conn.execute("""
            UPDATE publications SET
                drive_folder_id=?, drive_folder_url=?,
                drive_image_id=?, drive_image_url=?,
                drive_video_id=?, drive_video_url=?
            WHERE id=?
        """, (
            pub.drive_folder_id, pub.drive_folder_url,
            pub.drive_image_id, pub.drive_image_url,
            pub.drive_video_id, pub.drive_video_url,
            pub.pub_id,
        ))
        conn.commit()
        conn.close()

        # 6. Append to Google Sheet
        sheet_id = _get_or_create_spreadsheet(sheets_svc, drive_svc)
        _append_to_sheet(sheets_svc, sheet_id, pub)

        logger.info(
            "google_sync_complete",
            pub_id=pub.pub_id,
            folder_url=folder_url,
            sheet_id=sheet_id,
        )


