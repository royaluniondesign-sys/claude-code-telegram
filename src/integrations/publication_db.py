"""Publication Database — SQLite + Google Sheets sync.

Tracks every social post generated by AURA:
  - Stores locally in SQLite immediately (zero dependencies)
  - Syncs to Google Sheets when credentials are configured
  - Organizes files in Drive folders by scheduled publication time

SQLite path: ~/.aura/publications.db
Credentials: ~/.aura/google_credentials.json (service account or OAuth token)
Spreadsheet ID: stored in ~/.aura/google_sheets_id.txt after first sync

Usage:
    from src.integrations.publication_db import PublicationDB
    db = PublicationDB()
    pub_id = await db.log_publication(
        platform="instagram",
        format="1:1",
        topic="Claude Code vs GPT-4",
        headline="GPT-4 pierde contra Claude",
        subheadline="Prueba real de codigo en 2026",
        caption="El texto completo del post...",
        tag="AI TOOLS",
        image_bytes=png_bytes,           # saved to Drive
        scheduled_at=None,               # or datetime for scheduled posts
        status="generated",
    )
"""

